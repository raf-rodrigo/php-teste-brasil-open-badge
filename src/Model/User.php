<?php

/**
 * Classe referente a tabela users onde seus atributos(colunas) estão tipados
 */
class User
{
    /**
     * Variável da coluna de identificação do usuário (id)
     * @var integer
     */
    public $id;

    /**
     * Variável da coluna de name do usuário
     * @var string
     */
    public $name;

    /**
     * Variável da coluna de email do usuário
     * @var string
     */
    public $email;

}